//查看所有预约
void Teacher::showAllOrder()
{
	OrderFile of;
	if (of.m_Size == 0)
	{
		cout << "无预约记录!" << endl;
		system("pause");
		system("cls");
		return;
	}
	for (int i = 0; i < of.m_Size; i++)
	{
		cout << i + 1 << "         ";
		cout << "预约日期: 周" << of.m_orderData[i]["date"] << "  ";
		cout << "时间段: " << (of.m_orderData[i]["interval"] == "1" ? "上午" : "下午") << "  ";
		cout << "学生学号: " << of.m_orderData[i]["stuId"] << " ";
		cout << "学生姓名: " << of.m_orderData[i]["stuName"] << " ";

		cout << "机房编号: " << of.m_orderData[i]["roomId"] << "  ";
		string status = "状态: ";
		// 1 审核中   2 已预约  -1.预约失败   0 取消预约
		if (of.m_orderData[i]["status"] == "1")
			status += "审核中";
		else if (of.m_orderData[i]["status"] == "2")
			status += "预约成功";
		else if (of.m_orderData[i]["status"] == "-1")
			status += "预约失败,审核未通过";
		else {
			status += "取消预约";
		}
		cout << status << endl;
	}
	system("pause");
	system("cls");
}

//审核预约
void Teacher::valiOrder()
{
	OrderFile of;
	if (of.m_Size == 0)
	{
		cout << "无预约记录!" << endl;
		system("pause");
		system("cls");
		return;
	}
	cout << "待审核的预约记录如下" << endl;
	int index = 0;
	vector<int> v;              //存放在最大容器中的下标编号
	for (int i = 0; i < of.m_Size; i++)
	{
		if (of.m_orderData[i]["status"] == "1")
		{
			v.push_back(i);
			cout << ++index << "     ";
			cout << "预约日期: 周" << of.m_orderData[i]["date"] << "  ";
			cout << "时间段: " << (of.m_orderData[i]["interval"] == "1" ? "上午" : "下午") << "  ";
			cout << "学生学号: " << of.m_orderData[i]["stuId"] << " ";
			cout << "学生姓名: " << of.m_orderData[i]["stuName"] << " ";
			cout << "机房编号: " << of.m_orderData[i]["roomId"] << "  ";
			cout << "审核状态: 审核中" << endl;
		}
	}
	vector<int>::size_type select = 0;
	int ret = 0;    //接收老师审核结果
	while (true)
	{
		cout << "请输入审核的预约记录(0代表返回): ";
		cin >> select;        //接收用户的输入
		if (select >= 0 && select <= v.size())
		{
			if (select == 0)
				break;
			else
			{
				cout << "请输入审核结果(1.通过    2.不通过): ";
				cin >> ret;
				if (ret == 1)
				{
					//通过情况
					of.m_orderData[v[select - 1]]["status"] = "2";
				}
				else
				{
					//不通过情况
					of.m_orderData[v[select - 1]]["status"] = "-1";
				}
				of.updateOrder();         //更新预约记录
				cout << "审核完毕" << endl;
				break;
			}
		}
		cout << "输入有误，请重新输入!" << endl;
	}
	system("pause");
	system("cls");
}